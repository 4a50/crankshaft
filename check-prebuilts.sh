#!/bin/bash

BASEDIR="`cd $0 >/dev/null 2>&1; pwd`" >/dev/null 2>&1

if [ -d $BASEDIR/prebuilts ]; then
    echo "***************************************************************************************"
    echo "Checking for prebuilt updates..."
    echo "***************************************************************************************"
    cd prebuilts
    git reset --hard HEAD~1
    git clean -f -d
    git pull
    cd $BASEDIR
else
    echo "***************************************************************************************"
    echo "Downloading prebuilts..."
    echo "***************************************************************************************"
    git clone https://github.com/opencardev/prebuilts
    cd $BASEDIR
fi
echo "***************************************************************************************"
echo "Done"
echo "***************************************************************************************"
